class Data {

    /* PRIVATE MEMBERS */
    private:
        
        /* FIELDS */
        /* ************************************************************** */
        
        double lat, lon, value;
        
        /* ************************************************************** */
    
    /* PUBLIC MEMBERS */
    public:
            
        /* CONSTRUCTORS */
        /* ************************************************************** */
        
        Data(){
            lat = 0;
            lon = 0;
            value = 0;
        }
        
        Data(double newLon, double newLat, double newValue){
            lon = newLon;
            lat = newLat;
            value = newValue;
        }
        
        /* ************************************************************** */
    
    
        /* METHODS */   
        /* ************************************************************** */
        
        void set(double newLon, double newLat, double newValue){
            lon = newLon;
            lat = newLat;
            value = newValue;
        }
        
        double getLat(){
            return lat;
        }
        
        double getLon(){
            return lon;
        }
        
        double getValue(){
            return value;
        }
        
        /* Add newValue to what was in value */
        inline void addTo(double newValue){
            value += newValue;
        }
        /* ************************************************************** */
};

